#include <stdio.h>
#include <sys/time.h>
#include <unistd.h>

long    get_time(void)
{
    struct timeval  tp;
    long            milliseconds;

    gettimeofday(&tp, NULL);
    milliseconds = tp.tv_sec * 1000;
    milliseconds += tp.tv_usec / 1000;
    return (milliseconds);
}

void		sleep_better(long nb_ms)
{
	long		time;

	time = get_time();

	while (get_time() - time < nb_ms)
		continue ;
}

int main(void)
{
    long start_time;
		
		// Remember when we started
    start_time = get_time();

    while (1)
    {
				// Print time from start, in ms
        printf("%ld\n", get_time() - start_time);

				// Sleep 200 times 1000 microseconds (1 millisecond)
		sleep_better(200);
        // usleep(200 * 1000);
    }
}